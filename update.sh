clear
echo "updated"
sleep 3
clear
sudo sh upgrade.sh
cd ..
sudo chmod 777 zip
sudo rm -rf zip
clear
echo "temp files cleared."
echo "Upgraded/Updated - OK"
echo 'updated/installed FULL - YES' >> log,txt
