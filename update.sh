sudo mkdir zip
sudo chmod 777 zip
echo "chmod 777 - OK"
echo "mkdir zip - OK"
echo 'chmod 777 1 YES' >> log,txt
echo 'mkdir zip 1 YES' >> log,txt
sleep 1
clear
cp zip.zip zip
